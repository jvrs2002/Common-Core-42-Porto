char    *strchr(const char *s, int c)
{
    char    *ptr;
    
    ptr = NULL;
    if (s == c)
    {
        ptr = (char *) s;
        return ptr;
    }
    else if (s != c)
    {
        while (s != c)   
            s++;
        
    }        
    
}